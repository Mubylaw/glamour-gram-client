export const textVariant = (delay, duration) => {
  return {
    hidden: {
      y: -50,
      opacity: 0,
    },
    show: {
      y: 0,
      opacity: 1,
      transition: {
        type: "spring",
        duration: duration,
        delay: delay,
      },
    },
  };
};

export const fadeIn = (direction, type, delay, duration, dist) => {
  return {
    hidden: {
      x: direction === "left" ? dist : direction === "right" ? -dist : 0,
      y: direction === "up" ? dist : direction === "down" ? -dist : 0,
      opacity: 0,
    },
    show: {
      x: 0,
      y: 0,
      opacity: 1,
      transition: {
        type: type,
        delay: delay,
        duration: duration,
        ease: "easeOut",
      },
    },
  };
};

export const zoomIn = (delay, duration) => {
  return {
    hidden: {
      scale: 0,
      opacity: 0,
    },
    show: {
      scale: 1,
      opacity: 1,
      transition: {
        type: "tween",
        delay: delay,
        duration: duration,
        ease: "easeOut",
      },
    },
  };
};

